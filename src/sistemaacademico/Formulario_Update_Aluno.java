    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        if(this.txtNome.getText().length() == 0 || this.txtCurso.getText().length() == 0
                || this.txtEndereco.getText().length() == 0
                || this.txtTelefone.getText().length() == 0
                || this.txtSexo.getText().length() == 0){
            this.lblError.setText("TODOS OS CAMPOS SÂO OBRIGATÓRIOS!");
        }else{
            Sentenca_Aluno s = new Sentenca_Aluno();
            
            s.setRA(this.lblRA.getText());
            s.setNome(this.txtNome.getText());
            s.setCurso(this.txtCurso.getText());
            s.setEndereco(this.txtEndereco.getText());
            s.setTelefone(this.txtTelefone.getText());
            s.setSexo(this.txtSexo.getText());
            
            if(Funcoes_Aluno.isUpdate(s)){
                setClean();
                Funcoes_Aluno.setListar("");
                JOptionPane.showMessageDialog(this, "Usuário Atualizado", "INFORMAÇÂO", JOptionPane.INFORMATION_MESSAGE);
            }else{
                JOptionPane.showMessageDialog(this, "Erro Interno ao Atualizar", "ERROR", JOptionPane.ERROR_MESSAGE);
            }
        }
        this.setVisible(false);
    }//GEN-LAST:event_btnEditarActionPerformed

